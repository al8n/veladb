pub(super) fn log2(mut sz: usize) -> usize {
    if sz < LOG2.len() {
        return LOG2[sz];
    }
    let mut pow = 10;
    sz >>= 10;
    while sz > 1 {
        sz >>= 1;
        pow += 1;
    }
    pow
}
